export function add(num1, num2) {
  let sum = num1 + num2;
  return sum;
}

export function subtract(num1, num2) {
  let difference = num1 - num2;
  return difference;
}


export const multiply = (num1, num2) => {
  let mult = num1 * num2;
  return  mult;
}

export const divide = (num1, num2) => {
  let div = num1 / num2;
  return div;
}

